package de.sp.protocols.w2ui.forms.upload;

import java.util.Date;

public class UploadRequestFile {
	String name;
	String type;
	Date modified;
	int size;
	String content;
	public String getName() {
		return name;
	}
	public String getType() {
		return type;
	}
	public Date getModified() {
		return modified;
	}
	public int getSize() {
		return size;
	}
	public String getContent() {
		return content;
	}
	
	
	
}
